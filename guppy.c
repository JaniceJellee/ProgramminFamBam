#pragma config(Motor,  port2,           DFL,           tmotorVex393, openLoop)
#pragma config(Motor,  port3,           DFR,           tmotorVex393, openLoop)
#pragma config(Motor,  port4,           DBL,           tmotorVex393, openLoop)
#pragma config(Motor,  port5,           DBR,           tmotorVex393, openLoop, reversed)
#pragma config(Motor,  port6,           rightIntake,   tmotorVex393, openLoop, reversed)
#pragma config(Motor,  port7,           leftIntake,    tmotorVex393, openLoop)
#pragma config(Motor,  port8,           leftArm,       tmotorVex393, openLoop)
#pragma config(Motor,  port9,           rightArm,      tmotorVex393, openLoop)
